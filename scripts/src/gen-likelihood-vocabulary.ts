/**
 * Canonical RAID/risk likelihood vocabulary generator.
 *
 * The likelihood grades are authored as a single JSON ARRAY (lib/backend-catalogue/assets/
 * likelihood-vocabulary.json) — a flat vocabulary, so one array reads better than a file per token. This
 * validates every entry against assets/schema/likelihood-vocabulary.schema.json and emits the portable
 * lib/backend-catalogue/src/likelihood-vocabulary.generated.ts via the shared gen-registry engine (its
 * array-source variant) — the same generate-and-drift-guard pattern as gen-severity-vocabulary.
 *
 * Run: pnpm --filter @workspace/scripts run gen-likelihood-vocabulary
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { runArrayAssetGenerator } from "./lib/gen-registry";

runArrayAssetGenerator({
  label: "likelihood-vocabulary",
  schemaFile: "likelihood-vocabulary.schema.json",
  constName: "LIKELIHOOD_VOCABULARY_DATA",
  typeName: "LikelihoodVocabEntry",
  typeModule: "./likelihood-vocabulary",
  headerLines: [
    "/* GENERATED by scripts/src/gen-likelihood-vocabulary.ts — do not edit.",
    "   Source: lib/backend-catalogue/assets/likelihood-vocabulary.json;",
    "   run `pnpm --filter @workspace/scripts run gen-likelihood-vocabulary` to regenerate. */",
  ],
  loadRows: (root) =>
    JSON.parse(
      fs.readFileSync(path.join(root, "lib/backend-catalogue/assets/likelihood-vocabulary.json"), "utf8"),
    ) as Array<Record<string, unknown>>,
});
