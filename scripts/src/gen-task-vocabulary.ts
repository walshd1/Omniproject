/**
 * Canonical GTD task-status vocabulary generator.
 *
 * The GTD task statuses are authored as a single JSON ARRAY (lib/backend-catalogue/assets/
 * task-vocabulary.json) — a flat vocabulary, so one array reads better than a file per token. This
 * validates every entry against assets/schema/task-vocabulary.schema.json and emits the portable
 * lib/backend-catalogue/src/task-vocabulary.generated.ts via the shared gen-registry engine (its
 * array-source variant) — the same generate-and-drift-guard pattern as gen-work-vocabulary.
 *
 * Run: pnpm --filter @workspace/scripts run gen-task-vocabulary
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { runArrayAssetGenerator } from "./lib/gen-registry";

runArrayAssetGenerator({
  label: "task-vocabulary",
  schemaFile: "task-vocabulary.schema.json",
  constName: "TASK_VOCABULARY_DATA",
  typeName: "TaskVocabEntry",
  typeModule: "./task-vocabulary",
  headerLines: [
    "/* GENERATED by scripts/src/gen-task-vocabulary.ts — do not edit.",
    "   Source: lib/backend-catalogue/assets/task-vocabulary.json;",
    "   run `pnpm --filter @workspace/scripts run gen-task-vocabulary` to regenerate. */",
  ],
  loadRows: (root) =>
    JSON.parse(
      fs.readFileSync(path.join(root, "lib/backend-catalogue/assets/task-vocabulary.json"), "utf8"),
    ) as Array<Record<string, unknown>>,
});
