/**
 * Prioritisation-weights generator.
 *
 * The shipped default weight per prioritisation dimension is authored as a single JSON ARRAY of
 * {key, weight} pairs (lib/backend-catalogue/assets/priority-weights.json). This validates every
 * pair against assets/schema/priority-weight.schema.json and emits the portable
 * lib/backend-catalogue/src/priority-weights.generated.ts via the shared gen-registry engine —
 * the same generate-and-drift-guard pattern as fields and the work vocabulary.
 *
 * Run: pnpm --filter @workspace/scripts run gen-priority-weights
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { runArrayAssetGenerator } from "./lib/gen-registry";

runArrayAssetGenerator({
  label: "priority-weights",
  schemaFile: "priority-weight.schema.json",
  constName: "PRIORITY_WEIGHTS_DATA",
  typeName: "PriorityWeightSeed",
  typeModule: "./priority-weights",
  idField: "key",
  headerLines: [
    "/* GENERATED by scripts/src/gen-priority-weights.ts — do not edit.",
    "   Source: lib/backend-catalogue/assets/priority-weights.json;",
    "   run `pnpm --filter @workspace/scripts run gen-priority-weights` to regenerate. */",
  ],
  loadRows: (root) =>
    JSON.parse(
      fs.readFileSync(path.join(root, "lib/backend-catalogue/assets/priority-weights.json"), "utf8"),
    ) as Array<Record<string, unknown>>,
});
